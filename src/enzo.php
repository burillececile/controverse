                    <h3>Résumé des critères :</h3>
                    <p>
                        Pour résumer on peut donc dire que la valeur d’une <strong>œuvre</strong> d’art peut être défini
                        en <strong>5
                            critères</strong> majeurs tels que :
                        <br><br>
                        - <strong>L’artiste</strong> qui l’a réalisé.<br>
                        - <strong>Le matériau</strong> / le <strong>style</strong> utilisé afin de réaliser l’œuvre en
                        question.<br>
                        - <strong>Le nombre d’œuvres</strong> en vente / circulation sur le marché.<br>
                        - <strong>Le genre</strong> / <strong>style</strong> dans lequel s’<strong>inscrit</strong>
                        l’œuvre.<br>
