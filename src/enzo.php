                    <h3>Deux derniers critères :</h3>
                    <p>
                        Avec ces deux dernières œuvres, nous pouvons mettre en forme deux derniers critères de valeur
                        pour les
                        œuvres d’art.<br><br>

                        <strong>Premièrement</strong>, le genre de l’œuvre.<br>
                        En fonction du temps, les <strong>genres</strong> (principalement au niveau de la peinture) se
                        sont
                        retrouvés <strong>plus
                            ou moins influents</strong>, si bien que s’en peut <strong>modifier</strong> leurs <strong>prix</strong>
                        en fonction
                        de la
                        demande générale.
                        <br><br>
                        <strong>Deuxièmement</strong>, si l’œuvre est <strong>subversive</strong>. <br>
                        Au fil de nos recherches nous avons remarqué que les <strong>œuvres</strong> à caractère
                        <strong>subversives</strong> ou <strong>dénoncent</strong>
                        avait une <strong>plus valu significative</strong> par rapport aux œuvres dite « <strong>classique</strong>
                        ».<br>
                        Pour illustrer ce <strong>dernier critère</strong> de valeur, nous avons donc choisi 2 <strong>œuvres</strong>
                        du
                        <strong>même
                            genre</strong> (pop art), mais pourtant ayant une <strong>valeur significativement
                            éloignée</strong>.<br>
                        L’œuvre dite « <strong>classique</strong> » Est plus jolie, simple, relaxante tandis que l’œuvre
                        subversive elle
                        consiste en un texte écrit sur fond blanc disant : « <em>All of the assholes are gone</em> »
                        <br><br>
                        Malgré le fait que l’<strong>œuvre</strong> « <strong>classique</strong> » est globalement plus
                        <strong>agréable</strong>,
                        c’est
                        l’œuvre <strong>subversive</strong> qui va afficher le <strong>prix</strong> le plus <strong>onéreux</strong>.

                    </p>
                </div>
